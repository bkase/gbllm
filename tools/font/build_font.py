def parse_glyph_source() -> dict[str, list[str]]:
    glyphs: dict[str, list[str]] = {}
    for line in GLYPH_SOURCE.strip().splitlines():
        glyph, *rows = line.split("|")
        assert len(glyph) == 1
        assert len(rows) == 7
        assert all(len(row) == 5 for row in rows)
        glyphs[glyph] = rows
    return glyphs


GLYPHS = parse_glyph_source()


def pattern_to_bits(pattern: str) -> int:
    assert len(pattern) == 5
    bits = 0
    for column, pixel in enumerate(pattern):
        if pixel == "#":
            bits |= 1 << (6 - column)
    return bits


def glyph_rows(glyph: int) -> list[int]:
    if glyph <= 0x20:
        return [0] * 8

    pattern = GLYPHS.get(chr(glyph), GLYPHS["?"])
    return [0, *(pattern_to_bits(row) for row in pattern)]


def build() -> bytes:
    out = bytearray()
    for glyph in range(FONT_TILE_COUNT):
        for row in glyph_rows(glyph):
            out.append(row)
            out.append(row)
    assert len(out) == FONT_TILE_COUNT * BYTES_PER_TILE
    return bytes(out)


def main() -> None:
    if len(sys.argv) > 2:
        raise SystemExit(f"usage: {sys.argv[0]} [output-path]")
    out_path = Path(sys.argv[1]) if len(sys.argv) == 2 else OUT
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_bytes(build())


if __name__ == "__main__":
    main()
