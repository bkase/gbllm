#![forbid(unsafe_code)]

use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command as ProcessCommand;

use clap::{Parser, Subcommand};
use sha2::{Digest, Sha256};

#[derive(Debug, Parser)]
#[command(name = "xtask", color = clap::ColorChoice::Never)]
struct Args {
    #[command(subcommand)]
    command: Command,
}

#[derive(Debug, Subcommand)]
enum Command {
    RegenReviewPacket {
        #[arg(long)]
        feature: String,
    },
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    match Args::parse().command {
        Command::RegenReviewPacket { feature } if feature == "F-A8" => regen_f_a8(),
        Command::RegenReviewPacket { feature } => {
            Err(format!("unsupported review packet feature {feature}; expected F-A8").into())
        }
    }
}

