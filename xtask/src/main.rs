fn write(path: &Path, contents: &str) -> Result<(), Box<dyn std::error::Error>> {
    fs::write(path, contents)?;
    Ok(())
}

fn run_capture(
    cwd: &Path,
    program: &str,
    args: &[&str],
) -> Result<String, Box<dyn std::error::Error>> {
    let output = ProcessCommand::new(program)
        .args(args)
        .current_dir(cwd)
        .output()?;
    if !output.status.success() {
        return Err(format!(
            "{program} {} failed with status {}:\n{}",
            args.join(" "),
            output.status,
            String::from_utf8_lossy(&output.stderr)
        )
        .into());
    }
    Ok(String::from_utf8_lossy(&output.stdout).into_owned())
}

fn generate_repro_hashes(root: &Path) -> Result<String, Box<dyn std::error::Error>> {
    let dir = std::env::temp_dir().join(format!("gbf-debug-f-a8-review-{}", std::process::id()));
    let _ = fs::remove_dir_all(&dir);
    fs::create_dir_all(&dir)?;
    let s0 = dir.join("s0.gbsess");
    let s1 = dir.join("s1.gbsess");
    let script = dir.join("script.js");
    fs::write(
        &script,
        "gb.step(1);\nglobalThis.result = { pc: gb.regs.pc };\n",
    )?;
    let cargo = std::env::var("CARGO").unwrap_or_else(|_| "cargo".to_owned());
    let init_out = ProcessCommand::new(&cargo)
        .current_dir(root)
        .args([
            "run",
            "-q",
            "-p",
            "gbf-debug",
            "--",
            "init",
            "--rom",
            root.join("gbf-emu/tests/fixtures/tiny_rom.gb")
                .to_str()
                .ok_or("non-utf8 rom path")?,
            "--sym",
            root.join("docs/review/f-a1/artifacts/tiny_rom.sym")
                .to_str()
                .ok_or("non-utf8 sym path")?,
            "--out",
            s0.to_str().ok_or("non-utf8 session path")?,
        ])
        .output()?;
    if !init_out.status.success() {
        return Err(format!(
            "gbf-debug init failed:\n{}",
            String::from_utf8_lossy(&init_out.stderr)
        )
        .into());
    }
    let exec_out = ProcessCommand::new(&cargo)
        .current_dir(root)
        .args([
            "run",
            "-q",
            "-p",
            "gbf-debug",
            "--",
            "exec",
            "--in",
            s0.to_str().ok_or("non-utf8 input session path")?,
            "--script",
            script.to_str().ok_or("non-utf8 script path")?,
            "--out",
            s1.to_str().ok_or("non-utf8 output session path")?,
        ])
        .output()?;
    if !exec_out.status.success() {
        return Err(format!(
            "gbf-debug exec failed:\n{}",
            String::from_utf8_lossy(&exec_out.stderr)
        )
        .into());
    }
    let temp_prefix = dir.to_string_lossy();
    let init_stdout = String::from_utf8_lossy(&init_out.stdout)
        .replace(temp_prefix.as_ref(), "$TMP/gbf-debug-f-a8-review");
    let exec_stdout = String::from_utf8_lossy(&exec_out.stdout)
        .replace(temp_prefix.as_ref(), "$TMP/gbf-debug-f-a8-review");
    let report = format!(
        "init stdout: {}\nexec stdout: {}\ns0.gbsess sha256: {}\ns1.gbsess sha256: {}",
        init_stdout.trim(),
        exec_stdout.trim(),
        sha256_hex(&fs::read(&s0)?),
        sha256_hex(&fs::read(&s1)?)
    );
    let _ = fs::remove_dir_all(&dir);
    Ok(report)
}

fn sha256_hex(bytes: &[u8]) -> String {
    let digest = Sha256::digest(bytes);
    digest.iter().map(|byte| format!("{byte:02x}")).collect()
}

fn unsafe_report(src: &Path) -> Result<String, Box<dyn std::error::Error>> {
    let mut findings = Vec::new();
    for path in rust_files(src)? {
        let text = fs::read_to_string(&path)?;
        for (idx, line) in text.lines().enumerate() {
            let trimmed = line.trim();
            if trimmed.starts_with("unsafe ")
                || trimmed.contains(" unsafe ")
                || trimmed.contains("unsafe{")
                || trimmed.contains("unsafe {")
            {
                findings.push(format!(
                    "{}:{}: {}",
                    path.strip_prefix(src)?.display(),
                    idx + 1,
                    trimmed
                ));
            }
        }
    }
    if findings.is_empty() {
        Ok("No unsafe blocks or unsafe functions found under gbf-debug/src.\n".to_owned())
    } else {
        Ok(findings.join("\n") + "\n")
    }
}

fn rust_files(root: &Path) -> Result<Vec<PathBuf>, Box<dyn std::error::Error>> {
    let mut out = Vec::new();
    for entry in fs::read_dir(root)? {
        let entry = entry?;
        let path = entry.path();
        if path.is_dir() {
            out.extend(rust_files(&path)?);
        } else if path.extension().and_then(|ext| ext.to_str()) == Some("rs") {
            out.push(path);
        }
    }
    out.sort();
    Ok(out)
}
