fn main() -> Result<(), Box<dyn std::error::Error>> {
    let root = workspace_root();
    let artifacts = root.join("docs/review/f-b1/artifacts");
    if artifacts.exists() {
        fs::remove_dir_all(&artifacts)?;
    }
    fs::create_dir_all(&artifacts)?;

    let workload = MatmulI8BringupWorkload::default();
    let runs = workload.run()?;
    let conformance = byte_exact_matches(&runs)?;
    let report = normalize_report_self_hash(
        workload.report_from_runs_with_conformance(&runs, &conformance)?,
    )?;
    write(
        &artifacts.join("realism_report.v1.json"),
        report.to_canonical_json()?.as_bytes(),
    )?;

    let request = ComputeBringupRequest::headline_n128();
    let run = runs
        .iter()
        .find(|run| run.dim.n() == 128)
        .expect("default workload includes N=128");
    let streaming_rom = build_l3_streaming_rom(&request)?;
    write(
        &artifacts.join("matmul_n128_streaming.gb"),
        &streaming_rom.rom,
    )?;
    write(
        &artifacts.join("matmul_n128_streaming.sha256"),
        format!("{}\n", hash_bytes(&streaming_rom.rom)).as_bytes(),
    )?;
    write(
        &artifacts.join("matmul_n128_streaming.sym"),
        format!(
            "00:0150 f_b1.l3_streaming_entry\n00:{:04X} f_b1.tile_safe_point\n00:{:04X} f_b1.compute_yield_safe_point\n00:{:04X} f_b1.copy_yield_safe_point\n00:{:04X} f_b1.vblank_handler\n01:4000 f_b1.operand_a\n02:4000 f_b1.operand_b\n",
            streaming_rom.tile_safe_point_pc,
            streaming_rom.compute_yield_safe_point_pc,
            streaming_rom.copy_yield_safe_point_pc,
            streaming_rom.vblank_handler_pc
        )
        .as_bytes(),
    )?;
    write(
        &artifacts.join("matmul_n128_streaming.lst"),
        b"; F-B1 representative streaming ROM listing placeholder\n; Full instruction listing is generated by gbf-asm in follow-up listing plumbing.\n; One loaded ROM loops mt/nt/kt, performs BankLease(A/B) panel copies, accumulates one output tile, reaches f_b1.tile_safe_point, and resumes after the host DumpArena-shaped tile read.\n",
    )?;
    write_json(
        &artifacts.join("matmul_n128.map.json"),
        &json!({
            "source_wram_addr": 0xC000,
            "byte_len": 1024,
            "tile_internal_layout": "row_major_i32_le",
            "execution_model": "single_emulated_streaming_rom_dump_arena_safe_points",
            "representative_rom": "matmul_n128_streaming.gb",
            "tile_safe_point_pc": streaming_rom.tile_safe_point_pc,
            "compute_yield_safe_point_pc": streaming_rom.compute_yield_safe_point_pc,
            "copy_yield_safe_point_pc": streaming_rom.copy_yield_safe_point_pc,
            "vblank_handler_pc": streaming_rom.vblank_handler_pc,
            "yield_quantum": format!("{:?}", request.yield_quantum),
            "kernel": {"bank": 0, "offset": 0x2000},
            "multiply_kernel": {
                "algorithm": EMITTED_MULTIPLY_KERNEL_ID,
                "bank0_table_bytes": 0
            },
            "operand_a": {"bank": 1, "offset": 0, "bytes": 16384},
            "operand_b": {"bank": 2, "offset": 0, "bytes": 16384}
        }),
    )?;
    write(
        &artifacts.join("frame_trace_n128.summary.md"),
        frame_trace_summary(run)?.as_bytes(),
    )?;
    write_json(
        &artifacts.join("tile_dump_index_n128.json"),
        &json!({
            "tile_count": run.output_tiles.len(),
            "tile_bytes": 1024,
            "order": "mt_major_nt_minor",
            "tiles": run.output_tiles.iter().map(|tile| json!({
                "tile_index": tile.tile_index,
                "mt": tile.mt,
                "nt": tile.nt,
                "source_wram_addr": tile.source_wram_addr,
                "sha256": hash_bytes(&tile.bytes)
            })).collect::<Vec<_>>()
        }),
    )?;
    write_json(
        &artifacts.join("cycle_attribution_n128.json"),
        &json!({
            "cycle_unit": "mcycle",
            "dump_pause_excluded_from_compute_costs": true,
            "vblank_isr_included_in_end_to_end_frames": true,
            "products": run.metrics.products,
            "cycles_per_product_accumulate": run.metrics.cycles_per_full_matmul_mcycles / run.metrics.products,
            "cycles_per_output_tile": run.metrics.cycles_per_full_matmul_mcycles / u64::from(run.metrics.output_tiles),
            "max_unyielded_compute_mcycles": run.metrics.max_unyielded_compute_mcycles
        }),
    )?;
    write_json(
        &artifacts.join("run_latest.log.summary.json"),
        &json!({
            "tracing_event_counts": {
                "BankLeaseAcquired": run.metrics.bank_lease_acquire_count,
                "BankLeaseReleased": run.metrics.bank_lease_release_count,
                "WidgetTickDispatched": run.metrics.widget_update_count,
                "SchedulerServicedFrame": run.metrics.scheduler_service_count
            },
            "rust_log": "info,gbf=debug"
        }),
    )?;
    write(
        &artifacts.join("run_latest.log"),
        run_latest_log(&runs, run, &streaming_rom.rom).as_bytes(),
    )?;

    Ok(())
}

fn normalize_report_self_hash(
    report: RealismReportV1,
) -> Result<RealismReportV1, Box<dyn std::error::Error>> {
    let json = report.to_canonical_json()?;
    let decoded: RealismReportV1 = serde_json::from_str(&json)?;
    let decoded = decoded.with_computed_self_hash()?;
    decoded.validate_checked()?;
    Ok(decoded)
}

fn run_latest_log(
    runs: &[gbf_codegen::f_b1::BringupRun],
    headline: &gbf_codegen::f_b1::BringupRun,
    headline_rom: &[u8],
) -> String {
    let mut log = String::new();
    log.push_str("target=gbf::fb1 event=ReviewPacketRegenStarted schema=realism_report.v1\n");
    for run in runs {
        log.push_str(&format!(
            "target=gbf::fb1 event=StreamingRunComplete n={} products={} output_tiles={} bank_leases={} frame_service_misses={} byte_exact_candidate=true cycles_per_full_matmul_mcycles={}\n",
            run.dim.n(),
            run.metrics.products,
            run.metrics.output_tiles,
            run.metrics.bank_lease_acquire_count,
            run.metrics.frame_service_misses,
            run.metrics.cycles_per_full_matmul_mcycles,
        ));
    }
    log.push_str(&format!(
        "target=gbf::fb1 event=BankLeaseAcquired count={}\n",
        headline.metrics.bank_lease_acquire_count
    ));
    log.push_str(&format!(
        "target=gbf::fb1 event=BankLeaseReleased count={}\n",
        headline.metrics.bank_lease_release_count
    ));
    log.push_str(&format!(
        "target=gbf::fb1 event=WidgetTickDispatched count={}\n",
        headline.metrics.widget_update_count
    ));
    log.push_str(&format!(
        "target=gbf::fb1 event=SchedulerServicedFrame count={}\n",
        headline.metrics.scheduler_service_count
    ));
    log.push_str(&format!(
        "target=gbf::fb1 event=RepresentativeRom sha256={}\n",
        hash_bytes(headline_rom)
    ));
    log.push_str("target=gbf::fb1 event=ReviewPacketRegenFinished\n");
    log
}

fn frame_trace_summary(
    run: &gbf_codegen::f_b1::BringupRun,
) -> Result<String, Box<dyn std::error::Error>> {
    let stats = FrameTraceSummaryStats::from_events(&run.frame_events);
    let expected_margin_samples = usize::try_from(run.metrics.scheduler_service_count)?;
    validate_frame_service_margin_samples(&stats, expected_margin_samples)
        .map_err(|message| std::io::Error::new(std::io::ErrorKind::InvalidData, message))?;
    let mut out = String::new();
    out.push_str("# N=128 Frame Trace Summary\n\n");
    out.push_str(
        "The full N=128 frame trace is generated in memory during packet \
         regeneration and consumed by the report/build checks, but it is not \
         checked in. The pretty JSON trace is about 45 MiB and adds little \
         review value beyond the aggregate invariants below.\n\n",
    );
    out.push_str("## What This Means\n\n");
    out.push_str("- The L4 frame-service proof does not depend on a committed raw trace blob.\n");
    out.push_str(
        "- `realism_report.v1.json` remains the machine-checked source for the hard gate.\n",
    );
    out.push_str(
        "- This summary pins the reviewer-facing shape: real VBlank events, scheduler/widget service for every gated frame, and bounded liveness gaps.\n\n",
    );
    out.push_str("## Counts\n\n");
    out.push_str("| Metric | Value |\n| --- | ---: |\n");
    out.push_str(&format!("| Matrix size | {} |\n", run.dim.n()));
    out.push_str(&format!(
        "| Transient frame events generated | {} |\n",
        run.frame_events.len()
    ));
    out.push_str(&format!(
        "| VBlankFired events | {} |\n",
        stats.vblank_events
    ));
    out.push_str(&format!(
        "| WidgetTickDispatched events | {} |\n",
        stats.widget_events
    ));
    out.push_str(&format!(
        "| SchedulerServicedFrame events | {} |\n",
        stats.scheduler_events
    ));
    out.push_str(&format!(
        "| YieldReturnedToScheduler events | {} |\n",
        stats.yield_return_events
    ));
    out.push_str(&format!(
        "| ComputeProgressEpochAdvanced events | {} |\n",
        stats.progress_events
    ));
    out.push_str(&format!(
        "| Frame service misses | {} |\n",
        run.metrics.frame_service_misses
    ));
    out.push_str(&format!(
        "| Widget updates | {} |\n",
        run.metrics.widget_update_count
    ));
    out.push_str(&format!(
        "| Scheduler services | {} |\n",
        run.metrics.scheduler_service_count
    ));
    out.push_str(&format!(
        "| Max no-progress frames | {} |\n",
        run.metrics.max_no_progress_frames
    ));
    out.push_str(&format!(
        "| Yield while BankLease active | {} |\n",
        run.metrics.yield_while_bank_lease_active_count
    ));
    out.push_str(&format!(
        "| Harness pause while BankLease active | {} |\n",
        run.metrics.harness_pause_while_bank_lease_active_count
    ));
    if let Some(first) = stats.first_vblank_frame {
        out.push_str(&format!("| First VBlank frame in trace | {} |\n", first));
    }
    if let Some(last) = stats.last_vblank_frame {
        out.push_str(&format!("| Last VBlank frame in trace | {} |\n", last));
    }
    if let Some(min) = stats.first_mcycle {
        out.push_str(&format!("| First event M-cycle | {} |\n", min));
    }
    if let Some(max) = stats.last_mcycle {
        out.push_str(&format!("| Last event M-cycle | {} |\n", max));
    }

    out.push_str("\n## Frame-Service Margin\n\n");
    out.push_str("| Metric | Value |\n| --- | ---: |\n");
    out.push_str(&format!(
        "| Remaining-frame samples | {} |\n",
        stats.remaining_frame_mcycles.len()
    ));
    if !stats.remaining_frame_mcycles.is_empty() {
        out.push_str(&format!(
            "| Min remaining M-cycles | {} |\n",
            stats.remaining_min()
        ));
        out.push_str(&format!(
            "| Mean remaining M-cycles | {:.2} |\n",
            stats.remaining_mean()
        ));
        out.push_str(&format!(
            "| P99 remaining M-cycles | {} |\n",
            stats.remaining_p99()
        ));
        out.push_str(&format!(
            "| Max remaining M-cycles | {} |\n",
            stats.remaining_max()
        ));
    }

    out.push_str("\n## Reproduction\n\n");
    out.push_str("```bash\n");
    out.push_str("cargo run -p gbf-test --bin f_b1_regen\n");
    out.push_str("scripts/review/f-b1/verify-packet.sh\n");
    out.push_str("```\n\n");
    out.push_str(
        "For interactive inspection, initialize the checked ROM with `gbf-debug` \
         and run `scripts/review/f-b1/debug-safe-point.js`; that script checks \
         the VBlank handler, copy/compute/tile safe points, and HRAM service counters.\n",
    );
    Ok(out)
}

#[derive(Default)]
struct FrameTraceSummaryStats {
    vblank_events: usize,
    widget_events: usize,
    scheduler_events: usize,
    yield_return_events: usize,
    progress_events: usize,
    first_vblank_frame: Option<u32>,
    last_vblank_frame: Option<u32>,
    first_mcycle: Option<u64>,
    last_mcycle: Option<u64>,
    remaining_frame_mcycles: Vec<i32>,
}

impl FrameTraceSummaryStats {
    fn from_events(events: &[gbf_codegen::f_b1::FrameEventEnvelope]) -> Self {
        let mut stats = Self::default();
        let mut vblank_mcycles_by_frame = BTreeMap::new();
        let mut scheduler_mcycles_by_frame = BTreeMap::new();
        for envelope in events {
            match &envelope.event {
                FrameEvent::VBlankFired {
                    frame,
                    mcycle_since_boot,
                } => {
                    stats.vblank_events += 1;
                    stats.first_vblank_frame = stats.first_vblank_frame.or(Some(*frame));
                    stats.last_vblank_frame = Some(*frame);
                    stats.observe_mcycle(*mcycle_since_boot);
                    vblank_mcycles_by_frame.insert(*frame, *mcycle_since_boot);
                }
                FrameEvent::WidgetTickDispatched {
                    mcycle_since_boot, ..
                } => {
                    stats.widget_events += 1;
                    stats.observe_mcycle(*mcycle_since_boot);
                }
                FrameEvent::SchedulerServicedFrame {
                    frame,
                    mcycle_since_boot,
                } => {
                    stats.scheduler_events += 1;
                    stats.observe_mcycle(*mcycle_since_boot);
                    scheduler_mcycles_by_frame.insert(*frame, *mcycle_since_boot);
                }
                FrameEvent::YieldReturnedToScheduler {
                    mcycle_since_boot, ..
                } => {
                    stats.yield_return_events += 1;
                    stats.observe_mcycle(*mcycle_since_boot);
                }
                FrameEvent::ComputeProgressEpochAdvanced {
                    mcycle_since_boot, ..
                } => {
                    stats.progress_events += 1;
                    stats.observe_mcycle(*mcycle_since_boot);
                }
            }
        }
        stats.remaining_frame_mcycles = scheduler_mcycles_by_frame
            .into_iter()
            .filter_map(|(frame, service_mcycle)| {
                let next_vblank = vblank_mcycles_by_frame
                    .get(&frame.saturating_add(1))
                    .copied()?;
                Some(signed_mcycle_delta_i32(next_vblank, service_mcycle))
            })
            .collect();
        stats.remaining_frame_mcycles.sort_unstable();
        stats
    }

    fn observe_mcycle(&mut self, mcycle: u64) {
        self.first_mcycle = Some(self.first_mcycle.map_or(mcycle, |first| first.min(mcycle)));
        self.last_mcycle = Some(self.last_mcycle.map_or(mcycle, |last| last.max(mcycle)));
    }

    fn remaining_min(&self) -> i32 {
        *self
            .remaining_frame_mcycles
            .first()
            .expect("checked non-empty")
    }

    fn remaining_mean(&self) -> f64 {
        let sum: i64 = self
            .remaining_frame_mcycles
            .iter()
            .map(|value| i64::from(*value))
            .sum();
        sum as f64 / self.remaining_frame_mcycles.len() as f64
    }

    fn remaining_p99(&self) -> i32 {
        percentile_nearest_rank(&self.remaining_frame_mcycles, 99)
    }

    fn remaining_max(&self) -> i32 {
        *self
            .remaining_frame_mcycles
            .last()
            .expect("checked non-empty")
    }
}

fn validate_frame_service_margin_samples(
    stats: &FrameTraceSummaryStats,
    expected_scheduler_services: usize,
) -> Result<(), String> {
    let actual = stats.remaining_frame_mcycles.len();
    if actual == expected_scheduler_services {
        return Ok(());
    }

    Err(format!(
        "frame trace summary missing service-margin samples: expected \
         {expected_scheduler_services} scheduler-service margins, derived {actual}"
    ))
}

fn signed_mcycle_delta_i32(deadline_mcycle: u64, observed_mcycle: u64) -> i32 {
    let delta = i128::from(deadline_mcycle) - i128::from(observed_mcycle);
    i32::try_from(delta).unwrap_or(if delta.is_negative() {
        i32::MIN
    } else {
        i32::MAX
    })
}

fn percentile_nearest_rank(sorted: &[i32], percentile: usize) -> i32 {
    assert!(!sorted.is_empty(), "percentile requires samples");
    let rank = sorted.len().saturating_mul(percentile).div_ceil(100);
    sorted[rank.saturating_sub(1).min(sorted.len() - 1)]
}

fn byte_exact_matches(
    runs: &[gbf_codegen::f_b1::BringupRun],
) -> Result<Vec<bool>, Box<dyn std::error::Error>> {
    runs.iter()
        .map(|run| {
            let a = deterministic_matrix_a(run.dim);
            let b = deterministic_matrix_b(run.dim);
            let expected = matmul_reference_i8(
                MatrixI8::new(run.dim, &a).expect("shape"),
                MatrixI8::new(run.dim, &b).expect("shape"),
            )?;
            Ok(run.output_i32 == expected.data())
        })
        .collect()
}

fn write(path: &Path, bytes: &[u8]) -> Result<(), Box<dyn std::error::Error>> {
    fs::write(path, bytes)?;
    Ok(())
}

fn write_json(
    path: &Path,
    value: &impl serde::Serialize,
) -> Result<(), Box<dyn std::error::Error>> {
    let text = serde_json::to_string_pretty(value)?;
    write(path, format!("{text}\n").as_bytes())
}

fn hash_bytes(bytes: &[u8]) -> String {
    let mut hasher = Sha256::new();
    hasher.update(bytes);
    format!("sha256:{}", Hash256::from_bytes(hasher.finalize().into()))
}

fn workspace_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("workspace root")
        .to_path_buf()
}

#[cfg(test)]
mod tests {
    use gbf_codegen::f_b1::{FrameEvent, FrameEventEnvelope};

    use super::{
        FrameTraceSummaryStats, signed_mcycle_delta_i32, validate_frame_service_margin_samples,
    };

    #[test]
    fn signed_mcycle_delta_preserves_late_frame_margin() {
        assert_eq!(signed_mcycle_delta_i32(17_556, 17_000), 556);
        assert_eq!(signed_mcycle_delta_i32(17_556, 17_700), -144);
    }

    #[test]
    fn frame_service_margin_validation_catches_missing_next_vblank() {
        let stats = FrameTraceSummaryStats::from_events(&[
            FrameEventEnvelope {
                seq: 0,
                event: FrameEvent::VBlankFired {
                    frame: 2,
                    mcycle_since_boot: 10_000,
                },
            },
            FrameEventEnvelope {
                seq: 1,
                event: FrameEvent::SchedulerServicedFrame {
                    frame: 2,
                    mcycle_since_boot: 10_100,
                },
            },
        ]);

        let err = validate_frame_service_margin_samples(&stats, 1).expect_err("missing sample");
        assert!(err.contains("expected 1 scheduler-service margins"));
    }
}
