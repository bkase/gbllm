use gbf_bench::workload::MatmulI8BringupWorkload;
use gbf_codegen::f_b1::{
    ComputeBringupRequest, EMITTED_MULTIPLY_KERNEL_ID, FrameEvent, build_l3_streaming_rom,
};
use gbf_foundation::Hash256;
use gbf_report::realism::RealismReportV1;
