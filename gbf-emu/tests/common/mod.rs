#![allow(dead_code)]

use std::fs;

pub const TINY_ROM: &[u8] = include_bytes!("../fixtures/tiny_rom.gb");

pub fn tiny_rom() -> Vec<u8> {
    TINY_ROM.to_vec()
}

pub fn rom(program: &[u8], cartridge_type: u8, ram_size: u8) -> Vec<u8> {
    const LOGO: [u8; 48] = [
        0xCE, 0xED, 0x66, 0x66, 0xCC, 0x0D, 0x00, 0x0B, 0x03, 0x73, 0x00, 0x83, 0x00, 0x0C, 0x00,
        0x0D, 0x00, 0x08, 0x11, 0x1F, 0x88, 0x89, 0x00, 0x0E, 0xDC, 0xCC, 0x6E, 0xE6, 0xDD, 0xDD,
        0xD9, 0x99, 0xBB, 0xBB, 0x67, 0x63, 0x6E, 0x0E, 0xEC, 0xCC, 0xDD, 0xDC, 0x99, 0x9F, 0xBB,
        0xB9, 0x33, 0x3E,
    ];

    let mut rom = vec![0x00; 0x8000];
    rom[0x0100..0x0103].copy_from_slice(&[0xC3, 0x50, 0x01]);
    rom[0x0150..0x0150 + program.len()].copy_from_slice(program);
    rom[0x0104..=0x0133].copy_from_slice(&LOGO);
    rom[0x0134..0x013B].copy_from_slice(b"GBFEMU\0");
    rom[0x0147] = cartridge_type;
    rom[0x0148] = 0x00;
    rom[0x0149] = ram_size;
    rom[0x014D] = rom[0x0134..=0x014C]
        .iter()
        .fold(0_u8, |acc, byte| acc.wrapping_add(!byte));
    rom
}

pub fn workspace_file(path: &str) -> String {
    fs::read_to_string(path).unwrap_or_default()
}
